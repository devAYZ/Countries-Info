import Foundation

extension CGFloat {
    
    // MARK: Decimals
    static var pointTwo: CGFloat {
        return 0.2 as CGFloat
    }
    
    // MARK: Real numbet
    static var one: CGFloat {
        return 1 as CGFloat
    }
}
